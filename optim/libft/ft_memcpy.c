#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		*(t_byte*)dst++ = *(t_byte*)src++;
		i++;
	}
	return (dst - i);
}
