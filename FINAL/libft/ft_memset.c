#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;

	i = 0;
	while (i < len)
	{
		*(t_byte*)b = (t_byte)c;
		b++;
		i++;
	}
	return (b - i);
}
